public class ConceptualizationData {

    String infancia;
    String nucleares;
    String regras;
    String concepId;
    String patient_name;
    String patient_last_name;
    String user_type;

    public String getUser_type() {
        return user_type;
    }

    public void setUser_type(String user_type) {
        this.user_type = user_type;
    }



    public String getPatient_last_name() {
        return patient_last_name;
    }

    public void setPatient_last_name(String patient_last_name) {
        this.patient_last_name = patient_last_name;
    }



    public String getPatient_name() {
        return patient_name;
    }

    public void setPatient_name(String patient_name) {
        this.patient_name = patient_name;
    }


    public String getInfancia() {
        return infancia;
    }

    public void setInfancia(String infancia) {
        this.infancia = infancia;
    }

    public String getNucleares() {
        return nucleares;
    }

    public void setNucleares(String nucleares) {
        this.nucleares = nucleares;
    }

    public String getRegras() {
        return regras;
    }

    public void setRegras(String regras) {
        this.regras = regras;
    }

    public String getConcepId() {
        return concepId;
    }

    public void setConcepId(String concepId) {
        this.concepId = concepId;
    }



}
